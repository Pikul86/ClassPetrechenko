function getMathResult(num, times) {
    if (typeof(times) !== 'number' || times <= 0) {
        return num;
    }

    let str = '';

    for (let i = 1; i <= times; i++) {
        if (i === times) {
            str += `${num * i}`;
            // –¢—É—Ç –±–µ–∑ —á–µ—Ä—Ç–æ—á–µ–∫ –≤ –∫–æ–Ω—Ü–µ
        } else {
            str += `${num * i}---`;
            // –≠—Ç–æ —Ç–æ–∂–µ —Å–∞–º–æ–µ, —á—Ç–æ –∏
            // str = str + num * i + "---"
        }
    }

    return str;
}

